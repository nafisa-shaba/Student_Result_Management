    private void showInformation(String stu_Username, String stu_Password)
    {
        info_Username.setText(stu_Username);
        info_Password.setText(stu_Password);
        
        try{
            String sql = "select * from Student_Information where Username=? and Password=?";
            
            pst = conn.prepareStatement(sql);
            pst.setString(1,info_Username.getText());
            pst.setString(2,info_Password.getText());
            rs = pst.executeQuery();

            if(rs.next()){
                
                String add1 = rs.getString("Name");
                info_Name.setText(add1);
                
                String add2 = rs.getString("ID_No");
                info_ID.setText(add2);
                
                String add3 = rs.getString("Dept");
                info_Dept.setText(add3);
                
                String add4 = rs.getString("Date_of_Birth");
                info_DOB.setText(add4);
                
                String add5 = rs.getString("Phone_No");
                info_Phone.setText(add5);
                
                String add6 = rs.getString("Email");
                info_Email.setText(add6);
                
                String add7 = rs.getString("Username");
                info_Username.setText(add7);
                
                String add8 = rs.getString("Password");
                info_Password.setText(add8);
                             
            }

        }catch(Exception e){
            
        }
        
        try{
                
            String sql ="select * from Student_Result where Student_ID=? ";

            pst=conn.prepareStatement(sql); 
            pst.setString(1,info_ID.getText()); 
            rs=pst.executeQuery();
            result_Show.setModel(DbUtils.resultSetToTableModel(rs));
                      
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
        
    }
    
    
    

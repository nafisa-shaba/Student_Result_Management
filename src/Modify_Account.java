    private void mod_CheckActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mod_CheckActionPerformed
        
        String Username = (String)pre_Username.getText();
        String Password = (String)pre_Password.getText();
        
        new_Username.setEnabled(false);
        new_Password.setEnabled(false);
        re_Password.setEnabled(false);
        
        try{
            String sql = "select * from Admin_Account where Username=? and Password=?";
            
            pst = conn.prepareStatement(sql);
            pst.setString(1,pre_Username.getText());
            pst.setString(2,pre_Password.getText());
            rs = pst.executeQuery();

            if(rs.next()){
            
                checking.setText("Information Matched");
                
                new_Username.setEnabled(true);
                new_Password.setEnabled(true);
                re_Password.setEnabled(true);
                
            }
            else{
                JOptionPane.showMessageDialog(null, "Information Not Matched. \nRe-enter Information.");
                
            }

        }catch(Exception e){
            
        }
        
        
    }//GEN-LAST:event_mod_CheckActionPerformed

